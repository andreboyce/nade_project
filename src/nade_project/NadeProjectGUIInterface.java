    /**Connect to NADE server.*/
    public void ConnectToNADE()
    {
       try
       {
          if( nadeClient != null )
          {
              nadeClient.stopClient();
          }
          nadeClient = new NadeClient( this );
          nadeClient.runClient( this );
       }
       catch( IllegalThreadStateException e )
       {
           //jEditorPaneLog.append( e.getMessage() );
           display( "IllegalThreadStateException: " + e.getMessage() );
       }
       catch( Exception e )
       {
          //jEditorPaneLog.append( e.getMessage() );
           display( "Exception: " + e.getMessage() );
       }
    }

