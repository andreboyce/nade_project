private void jButtonStartNadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartNadeActionPerformed

   //java -classpath .;"lib\nade.jar" nade.server.NDServer
   //java -classpath .;"lib\nade.jar" nade.control.NDDefaultControl
   try
   {
      String      userDir = System.getProperty( "user.dir" );
      Runtime           r = Runtime.getRuntime();

      boolean bFoundNADEInCurrentDir = false;
      File NADELib = new File( userDir + "\\nade.jar" );

      bFoundNADEInCurrentDir = NADELib.exists();
      if( bFoundNADEInCurrentDir )
      {
         Process nadeserver  = r.exec( "javaw -classpath .;\"" + userDir + "/nade.jar\" nade.server.NDServer" );
         Process nadecontrol = r.exec( "javaw -classpath .;\"" + userDir + "/nade.jar\" nade.control.NDDefaultControl" );
      }
      else
      {
         Process nadeserver  = r.exec( "javaw -classpath .;\"" + userDir + "\\lib\\nade.jar\" nade.server.NDServer" );
         Process nadecontrol = r.exec( "javaw -classpath .;\"" + userDir + "\\lib\\nade.jar\" nade.control.NDDefaultControl" );
      }
   }
   catch( IOException e )
   {
      display( "NADE failed to start: " + e.getMessage() );
   }
   //catch( InterruptedException e )
   //{
   //   display( "NADE failed to start: " + e.getMessage() );
   //}
}//GEN-LAST:event_jButtonStartNadeActionPerformed

