    private void choiceGroupsItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_choiceGroupsItemStateChanged
       if( nadeClient != null )
       {
           nadeClient.UpdateGroups();
           GroupStatus gs = nadeClient.GetGroup( choiceGroups.getSelectedItem() );
           if( gs != null )
           {
              jTextFieldTheirMoney.setText(     String.valueOf( gs.getMoney())            );
              jTextFieldTheirResources.setText( String.valueOf( gs.getResources())        );
              jTextFieldTheirContracts.setText( String.valueOf( gs.getContractsWon())     );
              jTextFieldTheirHealth.setText(    String.valueOf( gs.getTrustWorthyLevel()) );
           }
       }
    }//GEN-LAST:event_choiceGroupsItemStateChanged

